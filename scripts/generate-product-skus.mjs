// scripts/generate-product-skus.mjs
//
// Generate data/db/product-skus.ts from the match results in
// scripts/analysis-output/gw-sku-map.json.
//
// Only "high" bucket matches are written; rows where the Puppeteer crawler
// disagreed with the matcher are excluded (they go to manual review).
//
// Usage: node scripts/generate-product-skus.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const MAP = path.join(__dirname, "analysis-output", "gw-sku-map.json");
const OUT = path.join(ROOT, "data", "db", "product-skus.ts");

const map = JSON.parse(fs.readFileSync(MAP, "utf8"));
const rows = map
  .filter((m) => m.bucket === "high" && m.canonicalSku && m.crawlAgrees !== false)
  .sort((a, b) => Number(a.productId) - Number(b.productId));

const esc = (s) => String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"');

const entries = rows.map((m) => {
  const aliases = new Set();
  if (m.gwSku && m.gwSku !== m.canonicalSku) aliases.add(m.gwSku);
  if (m.crawledSku && m.crawledSku !== m.canonicalSku) aliases.add(m.crawledSku);
  const names = new Set();
  if (m.gwName && m.gwName.trim() && m.gwName.trim().toLowerCase() !== m.name.trim().toLowerCase()) names.add(m.gwName.trim());

  let out = `  {\n    productId: "${esc(m.productId)}",\n    canonicalSku: "${esc(m.canonicalSku)}",`;
  if (aliases.size) out += `\n    skuAliases: [${[...aliases].map((a) => `"${esc(a)}"`).join(", ")}],`;
  if (names.size) out += `\n    nameAliases: [${[...names].map((n) => `"${esc(n)}"`).join(", ")}],`;
  out += `\n  },`;
  return out;
});

const file = `export type ProductSkuCatalogueEntry = {
  /** Internal Pricehammer product ID */
  productId: string;
  /** Manufacturer (Games Workshop) SKU when known */
  canonicalSku: string | null;
  /** Additional SKUs or retailer-specific aliases that map back to the product */
  skuAliases?: string[];
  /** Optional alternate names (e.g. "Start Collecting! Stormcast Eternals") */
  nameAliases?: string[];
};

/**
 * Canonical SKU catalogue linking the Pricehammer product database back to
 * Games Workshop identifiers.
 *
 * AUTO-GENERATED by scripts/generate-product-skus.mjs from the GW Algolia
 * catalogue match (scripts/analysis-output/gw-sku-map.json). High-confidence
 * matches only. Re-run the pipeline to refresh:
 *   node scripts/fetch-gw-catalogue.mjs
 *   node scripts/match-gw-skus.mjs
 *   node scripts/generate-product-skus.mjs
 */
export const ProductSkuCatalogue: ProductSkuCatalogueEntry[] = [
${entries.join("\n")}
];
`;

fs.writeFileSync(OUT, file);
console.log(`wrote ${rows.length} entries -> ${path.relative(ROOT, OUT)}`);
